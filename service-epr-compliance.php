                <h2>EPR Compliance Requirements</h2>
                <p>Under the E-Waste Management Rules 2022, manufacturers must:</p>
                <ul>
                    <li>Register with CPCB and obtain EPR authorization</li>
                    <li>Calculate EPR targets based on products placed in market</li>
                    <li>Set up collection centers or partner with authorized recyclers</li>
                    <li>Collect and recycle e-waste equivalent to EPR targets</li>
                    <li>Submit annual returns to CPCB with supporting documents</li>
                    <li>Maintain records of collection, recycling, and certificates</li>
                </ul>
